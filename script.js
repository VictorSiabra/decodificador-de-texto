function copiarTexto() {
    // Obtém o campo de texto
    var campoTexto = document.getElementById("campoTexto");

    // Seleciona o texto
    campoTexto.select();

    // Copia o texto para a área de transferência
    document.execCommand("copy");

    // Limpa o campo de texto
    campoTexto.value = "";

    // Exibe uma mensagem de confirmação (opcional)
    alert("Texto copiado e campo limpo!");
}

document.addEventListener("DOMContentLoaded", function() {
    const textarea = document.getElementById("campo");

    textarea.addEventListener("input", function() {
        const allowedCharacters = /^[a-zA-Z0-9\s]*$/;
        if (!allowedCharacters.test(textarea.value)) {
            textarea.value = textarea.value.replace(/[^a-zA-Z0-9\s]/g, '');
        }
    });
});